public class GetSetMetricConverts {
    private double gallons;
    private double liters;
    private double inches;
    private double centimeters;

    public void setGallons(double gallonAmount)
    {
        gallons = gallonAmount;
    }
    public double getGallons()
    {
        return gallons;
    }
    public void setLiters(double litersAmount)
    {
        liters = litersAmount;
    }
    public double getLiters()
    {
        return liters;
    }

    public void setInches(double inchesAmount)
    {
        inches = inchesAmount;
    }
    public double getInches()
    {
        return inches;
    }
    public void setCentimeters(double inchesAmount)
    {
        centimeters = inchesAmount;
    }
    public double getCentimeters()
    {
        return centimeters;
    }
}
